p1=[1/2,1/4,1/8,1/8];%p1代表甲信源对应的概率
p2=[7/8,1/8];%p2代表乙信源对应的概率
H1=0.0;%赋初值
H2=0.0; %赋初值
I=[];%赋初值
J=[];%赋初值
for i=1:4
    H1=H1+p1(i)*log2(1/p1(i));%计算公式
    I(i)=log2(1/p1(i));%计算公式
end
disp('自信息量分别为：');
